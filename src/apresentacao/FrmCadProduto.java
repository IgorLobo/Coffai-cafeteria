    private void limpar() {

        txtId.setText("");
        txtNome.setText("");
        txtCodBarras.setText("");
        txtValor.setText("");
        txtQnt.setText("");

        btnExcluir.setEnabled(false);

    }

    private void carregarTela(Produto produto) {

        txtId.setText(produto.getId() + "");
        txtNome.setText(produto.getNome());
        txtCodBarras.setText(produto.getCodBarras());
        txtValor.setText(Double.toString(produto.getValor()));
        txtQnt.setText(Integer.toString(produto.getQnt()));

        btnExcluir.setEnabled(true);

    }
